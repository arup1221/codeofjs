//conditional statement: Switch Statement

//syntax:
switch(Math.floor(Math.random()*5 +1)){
    case 1:
      console.log("one");
    break;
    case 2:
        console.log("two");
    break;
    case 3:
        console.log("three");
    break;
    case 4:
        console.log("four");
    break;
    default:
        console.log("No Match");
}